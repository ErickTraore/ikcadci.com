    Le seba est un certificat délivré dans le cadre du programme aux personnes ayant validées 1 cours semestriel d’un volume horaire de 24 heures.
    La kamitologie est le domaine des études africaines conduites dans une perspective africaine. Il s’agit de l’application de la théorie de l’afrocentricité, fondée par le professeur Kete Asante, dans le champ épistémologique. Le professeur Ama Mazama grande figure de l’école de l’afrocentricité est la conceptrice de ce programme académique de kamitologie de haut niveau dont la mise en œuvre est assurée, sous sa direction, par une équipe pédagogique qualifiée.
